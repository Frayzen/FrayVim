function snake(s)
  s = s:gsub(" ", "_")
  return s:gsub("%f[^%l]%u", "_%1")
      :gsub("%f[^%a]%d", "_%1")
      :gsub("%f[^%d]%a", "_%1")
      :gsub("(%u)(%u%l)", "%1_%2")
      :lower()
end

